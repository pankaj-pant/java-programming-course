package app;

/* Task: Create a program called PrintIntegers that prints integers from 1 to 20 to the console. 
The integers should be printed on a single line. Use a loop in your program. */

public class App {
    public static void main(String[] args) throws Exception {
        for (int i = 1; i < 21; i++) {
			System.out.print(i + " ");
	}
    }
}